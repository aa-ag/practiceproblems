
def lolw(s: str) -> int:
    return len(s.split()[-1])


# Input: s = "Hello World"
# Output: 5
print(lolw("Hello World"))

# Input: s = "   fly me   to   the moon  "
# Output: 4
print(lolw("   fly me   to   the moon  "))

# Input: s = "luffy is still joyboy"
# Output: 6
print(lolw("luffy is still joyboy"))