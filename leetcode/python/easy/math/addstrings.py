def add_strings(num1: str, num2: str) -> str:
    return str(int(num1) + int(num2))


# Input: num1 = "11", num2 = "123"
# Output: "134"
print(add_strings("11", "123"))

# Input: num1 = "456", num2 = "77"
# Output: "533"
print(add_strings("456", "77"))

# Input: num1 = "0", num2 = "0"
# Output: "0"
print(add_strings("0", "0"))